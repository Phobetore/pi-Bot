def get(lang: str) -> dict[str, str]:
    """Return the translation table for ``lang``, falling back to English."""
    return _TRANSLATIONS.get(lang, _TRANSLATIONS[DEFAULT_LANGUAGE])


def t(lang: str, key: str, /, **kwargs: object) -> str:
    """Translate ``key`` to ``lang``, formatting with ``kwargs``."""
    table = get(lang)
    template = table.get(key) or _TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key)
    if kwargs:
        return template.format(**kwargs)
    return template
